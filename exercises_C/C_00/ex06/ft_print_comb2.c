#include <unistd.h>

void	ft_print_comb2(void)
{
	char	nb[5];

	nb[0] = '0' - 1;
	nb[2] = ' ';
	while (nb[0]++ < '9')
	{
		nb[1] = '0' - 1;
		nb[3] = nb[0] - 1;
		nb[4] = nb[1] + 1;
		while (nb[1]++ < '9')
		{
			while (nb[3]++ < '9')
			{
				while (nb[4]++ < '9')
				{
					write(1, &nb, 5);
					if (nb[0] != '9' || nb[1] != '8')
						write(1, ", ", 2);
				}
				nb[4] = '0' - 1;
			}
			nb[3] = nb[0] - 1;
			nb[4] = nb[1] + 1;
		}
	}
}
